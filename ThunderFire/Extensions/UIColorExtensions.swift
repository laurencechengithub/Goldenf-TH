import Foundation
import UIKit


//make color extension
//UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 0.2)

extension UIColor {
    
    open class var THGreen: UIColor {
        return UIColor(red: 7/255, green: 214/255, blue: 160/255, alpha: 1)
    }
    
    open class var THGray: UIColor {
        return UIColor(red: 194/255, green: 206/255, blue: 220/255, alpha: 1)
    }
    
    
    
}
